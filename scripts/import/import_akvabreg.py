def import_materials():
    if not os.path.exists(EST_DB_PATH):
        print(f"[ERROR] Файл не найден: {EST_DB_PATH}")
        return
    
    conn = sqlite3.connect(EST_DB_PATH)
    c = conn.cursor()
    
    # Проверяем существует ли колонка category
    try:
        c.execute("SELECT category FROM catalog_materials LIMIT 1")
    except sqlite3.OperationalError:
        try:
            c.execute("ALTER TABLE catalog_materials ADD COLUMN category TEXT DEFAULT ''")
            conn.commit()
            print("[OK] Добавлена колонка category")
        except sqlite3.OperationalError:
            pass
    
    added = 0
    skipped = 0
    for name, unit, cat, purchase, retail, wholesale, min_qty, desc in materials:
        try:
            c.execute("INSERT INTO catalog_materials (name, unit, category, purchase_price, retail_price, wholesale_price, min_wholesale_qty, description, use_count) VALUES (?, ?, ?, ?, ?, ?, ?, ?, 0)",
                     (name, unit, cat, purchase, retail, wholesale, min_qty, desc))
            added += 1
        except sqlite3.IntegrityError:
            skipped += 1
    
    conn.commit()
    conn.close()
    
    print(f"[OK] Добавлено: {added} товаров")
    print(f"[SKIP] Пропущено (уже есть): {skipped} товаров")
    print(f"[INFO] Всего позиций импортировано: {len(materials)}")

if __name__ == '__main__':
    print("=" * 60)
    print("  Импорт каталога AkvaBreg.by")
    print("=" * 60)
    import_materials()
