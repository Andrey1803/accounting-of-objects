def determine_correct_type(name):
    """Определить правильный item_type по имени товара."""
    name_low = name.lower()
    for pattern, correct_type in TYPE_PATTERNS:
        if re.search(pattern, name_low):
            return correct_type
    return None  # Не удалось определить

def main():
    conn = sqlite3.connect(DB)
    c = conn.cursor()
    
    c.execute("SELECT id, name, category, item_type FROM catalog_materials WHERE user_id=1")
    all_items = c.fetchall()
    
    total = len(all_items)
    fixed = 0
    errors = []
    
    for item_id, name, category, current_type in all_items:
        correct_type = determine_correct_type(name)
        
        if correct_type and correct_type != current_type:
            c.execute("UPDATE catalog_materials SET item_type=? WHERE id=?", (correct_type, item_id))
            fixed += 1
            errors.append({
                'id': item_id,
                'name': name[:60],
                'category': category,
                'old_type': current_type,
                'new_type': correct_type
            })
    
    conn.commit()
    conn.close()
    
    print(f"Всего товаров: {total}")
    print(f"Исправлено item_type: {fixed}")
    print()
    
    if errors:
        print("Примеры исправлений (первые 30):")
        for e in errors[:30]:
            print(f"  [{e['id']}] {e['name']}")
            print(f"    {e['category']} | {e['old_type']} → {e['new_type']}")
            print()
        
        if len(errors) > 30:
            print(f"  ... и ещё {len(errors) - 30} исправлений")

if __name__ == '__main__':
    print("=" * 60)
    print("  Универсальная проверка item_type по ВСЕМ категориям")
    print("=" * 60)
    main()
