def main():
    conn = sqlite3.connect(DB)
    c = conn.cursor()

    # Получаем все материалы
    c.execute("SELECT id, name, category FROM catalog_materials WHERE user_id=1")
    materials = c.fetchall()

    import re
    updated = 0
    no_match = 0

    for mat_id, name, current_cat in materials:
        new_cat = None
        for keyword, target_cat in KEYWORD_MAP.items():
            if re.search(keyword, name, re.IGNORECASE):
                new_cat = target_cat
                break

        if new_cat and new_cat != current_cat:
            c.execute("UPDATE catalog_materials SET category=? WHERE id=?", (new_cat, mat_id))
            updated += 1
        elif not new_cat:
            no_match += 1

    conn.commit()
    conn.close()
    print(f"Обновлено категорий: {updated}")
    print(f"Не распознано: {no_match}")

if __name__ == '__main__':
    main()
