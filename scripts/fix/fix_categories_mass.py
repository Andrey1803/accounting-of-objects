def fix_category(name, current_cat):
    """Определить правильную category по имени товара."""
    name_low = name.lower()
    # Сортируем по длине паттерна (длинные = более специфичные — приоритетнее)
    sorted_patterns = sorted(CATEGORY_MAP.keys(), key=lambda x: len(x), reverse=True)
    for pattern in sorted_patterns:
        if re.search(pattern, name_low):
            return CATEGORY_MAP[pattern]
    return current_cat  # Оставляем текущую, если не нашли

def main():
    conn = sqlite3.connect(DB)
    c = conn.cursor()
    
    # Добавляем backup колонку для category если нет
    try:
        c.execute("ALTER TABLE catalog_materials ADD COLUMN original_category TEXT DEFAULT ''")
        print("Добавлена original_category")
    except:
        print("original_category уже существует")
    
    # Сохраняем оригиналы
    c.execute("""UPDATE catalog_materials 
                 SET original_category = COALESCE(category, '')
                 WHERE original_category = ''""")
    print(f"Сохранены оригиналы: {c.rowcount} строк")
    
    # Исправляем
    c.execute("SELECT id, name, category FROM catalog_materials WHERE user_id=1")
    all_items = c.fetchall()
    
    updated = 0
    for item_id, name, current_cat in all_items:
        new_cat = fix_category(name, current_cat)
        if new_cat != current_cat:
            c.execute("UPDATE catalog_materials SET category = ? WHERE id = ?", (new_cat, item_id))
            updated += 1
    
    conn.commit()
    conn.close()
    
    print(f"\n✅ Исправлена category: {updated} записей")
    print(f"\n🔄 Для отката:")
    print(f"   UPDATE catalog_materials SET category = original_category WHERE user_id=1;")

if __name__ == '__main__':
    print("=" * 60)
